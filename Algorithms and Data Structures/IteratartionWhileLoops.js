//can run the same code multiple times
//while loop run while a specified condition is true and stops once that condition is no longer true

var ourArray = [];
var i = 0;
while(i < 5) {
  ourArray.push(i);
  i++;
}

//descending
var myArray = [];

var i = 5;
while (i > -1) {
    myArray.push(i);
    i--;
}
